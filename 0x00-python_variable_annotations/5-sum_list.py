#!/usr/bin/env python3
''' 5-sum_list.py '''


from typing import List


def sum_list(input_list: List[float]) -> float:
    '''
        sum_list: Caclculate the sum of floats in an array
        Args:
            input_list: An array of floats
            Return: The sum of floats as a float
    '''
    return sum(input_list)
